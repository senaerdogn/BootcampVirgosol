package Day1;

public class Task2 {
    public static void main(String[] args) {
        String isim = "Ali";
        int yas = 23;
        String sehir = "İstanbul";

        System.out.println("Adım: " + isim + " Yaşım: " + yas);
        System.out.println(yas);
        System.out.println("Şehrim: " + sehir);
        System.out.println(isim);
    }
}
