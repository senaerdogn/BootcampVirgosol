package Day1;

public class Task8 {
    public static void main(String[] args) {
        int sayi1=15, sayi2=2;
        System.out.println(sayi1 + " + " + sayi2 + " = " + (sayi1+sayi2));
        System.out.println(sayi1 + " - " + sayi2 + " = " + (sayi1-sayi2));
        System.out.println(sayi1 + " * " + sayi2 + " = " + (sayi1*sayi2));
        System.out.println(sayi1 + " / " + sayi2 + " = " + (sayi1/sayi2));
        System.out.println(sayi1 + " % " + sayi2 + " = " + (sayi1%sayi2));

    }
}
